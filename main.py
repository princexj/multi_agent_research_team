# --- TASK 2: Implement arXiv Paper Search Function ---
def search_arxiv(query: str, max_results: int = 3) -> str:
    """
    Searches the arXiv database for academic papers based on a search query.
    Returns a formatted string containing the title, authors, link, and summary of the papers.
    """
    print(f"\n[Executing arXiv Search Tool] Searching for: '{query}'...\n")
    client = arxiv.Client()
    search = arxiv.Search(
        query=query,
        max_results=max_results,
        sort_by=arxiv.SortCriterion.Relevance
    )
    
    results = []
    for paper in client.results(search):
        paper_info = (
            f"Title: {paper.title}\n"
            # f"Authors: {', '.join([author.name for author in paper.authors])}\n"
            f"Link: {paper.entry_id}\n"
            f"Summary: {paper.summary}\n"
        )
        results.append(paper_info)

    if not results:
        return "No papers found for this query."
    return "\n----------------------------------------\n".join(results)


