# --- TASK 11: Implement Custom Selector and GroupChat ---
def state_transition(last_speaker, groupchat):
    """
    Traffic controller, updated to handle tool executions properly.
    """
    messages = groupchat.messages
    if not messages:
        return topic_agent
    
    last_message = messages[-1]
    
    # If the AI calls a tool, hand off to the user_proxy to execute it
    if "tool_calls" in last_message or "function_call" in last_message:
        return user_proxy
        
    # If the user_proxy just finished running the tool, pass the real arXiv results to the insight agent
    if last_speaker is user_proxy and last_message.get("role") == "tool":
        return insight_agent
        
    # Standard sequential assembly line
    if last_speaker is topic_agent:
        return discovery_agent
    if last_speaker is discovery_agent:
        return insight_agent
    if last_speaker is insight_agent:
        return report_agent
    if last_speaker is report_agent:
        return gap_agent
        
    # Hand off to the human at the very end
    if last_speaker is gap_agent:
        return user_proxy
        
    # Human approval logic
    if last_speaker is user_proxy:
        if "APPROVE" in last_message.get("content", "").upper():
            return None  # Ends the chat
        else:
            return topic_agent # Restarts the search based on feedback

# GroupChat logic
groupchat = autogen.GroupChat(
    agents=[user_proxy, topic_agent, discovery_agent, insight_agent, report_agent, gap_agent],
    messages=[],
    max_round=20
)

groupchat.speaker_selection_method = state_transition

manager = autogen.GroupChatManager(
    groupchat=groupchat,
    llm_config=llm_config,
    is_termination_msg=is_termination_message
)
print("Task 11: Custom Selector Function defined and Workflow updated.\n")


